public class Operators {
    public static void main(String[] args) {
        int sum = 10 + 20;
        System.out.println(sum);
        // Operator +, -, *, /, %

        int count = 0;
        // Increment
        count++;

        // Pre-increment
        ++count;

        // Assign
        count += 1;

        /*
            ==	Equal to	x == y	
            !=	Not equal	x != y	
            >	Greater than	x > y	
            <	Less than	x < y	
            >=	Greater than or equal to	x >= y	
            <=	Less than or equal to	x <= y
         */

    }
}
